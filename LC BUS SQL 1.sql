Drop Table Calendar;

CREATE TABLE Calendar (
    service_days        VARCHAR(20) NOT NULL,
    start_date          VARCHAR(20) NOT NULL,
    end_date            VARCHAR(20) NOT NULL);

ALTER TABLE Calendar
          ADD CONSTRAINT Calendar_pk
          PRIMARY KEY (service_days);

CREATE TABLE Fares (            
    fare_id             VARCHAR(1) NOT NULL,
    fare_description    VARCHAR(43) NOT NULL);

ALTER TABLE Fares
          ADD CONSTRAINT Fares_pk
          PRIMARY KEY (fare_id); 

CREATE TABLE agency (
    agency_id               VARCHAR(4) NOT NULL,
    agency_name             VARCHAR(50) NOT NULL);

ALTER TABLE agency
        ADD CONSTRAINT agency_pk
        PRIMARY KEY (agency_id);


CREATE TABLE agencyYard (
    agency_id               VARCHAR(4) NOT NULL,
    yard_number             VARCHAR(1) NOT NULL,
    yard_address           VARCHAR(50) NOT NULL,
    agency_phone_number     VARCHAR(20));  

ALTER TABLE agencyYard
        ADD CONSTRAINT agencyYard_pk
        PRIMARY KEY (yard_number);

ALTER TABLE agencyYard
        ADD CONSTRAINT agencyYard_agency_fk
        FOREIGN KEY(agency_id)
        REFERENCES agency (agency_id);

CREATE TABLE routes (
    route_id                VARCHAR(4) NOT NULL,
    agency_id               VARCHAR(4) NOT NULL,
    fare_id                 VARCHAR(1) NOT NULL,
    route_description       VARCHAR(50) NOT NULL,
    roads_used              VARCHAR(200),
    route_type              VARCHAR(20));

ALTER TABLE routes
        ADD CONSTRAINT routes_agency_fk
        FOREIGN KEY (agency_id)
        REFERENCES agency(agency_id);

ALTER TABLE routes
        ADD CONSTRAINT routes_Fares_fk
        FOREIGN KEY (fare_id)
        REFERENCES Fares(fare_id);

CREATE TABLE trips (
    route_id                VARCHAR(4) NOT NULL,
    agency_id               VARCHAR(4) NOT NULL,
    service_days            VARCHAR(20) NOT NULL,
    trip_id                 VARCHAR(10) NOT NULL,
    times                   VARCHAR(8) NOT NULL,
    trip_headsign           VARCHAR(70) NOT NULL,
    block_id                VARCHAR(6) NOT NULL);

ALTER TABLE trips
        ADD CONSTRAINT trips_agency_fk
        FOREIGN KEY (agency_id)
        REFERENCES agency(agency_id);

ALTER TABLE trips
        ADD CONSTRAINT trips_Calendar_fk
        FOREIGN KEY (service_days)
        REFERENCES Calendar(service_days);

